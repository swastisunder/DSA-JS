const calculator = {
  add: function (a, b) {
    return a + b;
  },

  sub: function (a, b) {
    return a - b;
  },

  mul: function (a, b) {
    return a * b;
  },
};

// Example usage:
console.log(calculator.add(5, 3)); // 8
console.log(calculator.sub(5, 3)); // 2
console.log(calculator.mul(5, 3)); // 15
